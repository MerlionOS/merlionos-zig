pub const hello = [_]u8{
    0x48, 0xc7, 0xc0, 0x01, 0x00, 0x00, 0x00, // mov rax, SYS_WRITE
    0x48, 0xc7, 0xc7, 0x01, 0x00, 0x00, 0x00, // mov rdi, stdout
    0x48, 0x8d, 0x35, 0x18, 0x00, 0x00, 0x00, // lea rsi, [rip + message]
    0x48, 0xc7, 0xc2, 0x13, 0x00, 0x00, 0x00, // mov rdx, message length
    0xcd, 0x80, // int 0x80
    0x48, 0xc7, 0xc0, 0x00, 0x00, 0x00, 0x00, // mov rax, SYS_EXIT
    0x48, 0x31, 0xff, // xor rdi, rdi
    0xcd, 0x80, // int 0x80
    0xf4, // hlt, should not run if SYS_EXIT works
    0xeb, 0xfd, // loop on hlt if SYS_EXIT unexpectedly returns
    'H',  'e',
    'l',  'l',
    'o',  ' ',
    'f',  'r',
    'o',  'm',
    ' ',  'R',
    'i',  'n',
    'g',  ' ',
    '3',  '!',
    '\n',
};

pub const loop_user = [_]u8{
    0x48, 0xc7, 0xc0, 0x01, 0x00, 0x00, 0x00, // mov rax, SYS_WRITE
    0x48, 0xc7, 0xc7, 0x01, 0x00, 0x00, 0x00, // mov rdi, stdout
    0x48, 0x8d, 0x35, 0x1d, 0x00, 0x00, 0x00, // lea rsi, [rip + message]
    0x48, 0xc7, 0xc2, 0x05, 0x00, 0x00, 0x00, // mov rdx, message length
    0xcd, 0x80, // int 0x80
    0xb9, 0x00, 0x00, 0x01, 0x00, // mov ecx, 0x10000
    0xf3, 0x90, // pause
    0xe2, 0xfc, // loop -4
    0x48, 0xc7, 0xc0, 0x03, 0x00, 0x00, 0x00, // mov rax, SYS_YIELD
    0xcd, 0x80, // int 0x80
    0xeb, 0xce, // jmp start
    't',  'i',
    'c',  'k',
    '\n',
};

